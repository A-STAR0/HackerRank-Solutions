// List of bitwise operators in c++: 

// Left shift: [variable]<<[number of places to shift the bits] - equivalent to integer multiplication by a power of 2
// Right shift: [variable]>>[number of places to shift the bits] - equivalent to integer division by a power of 2


// Note the first two are similar to && and ||, which we use to represent and/or in conditional statements
// Bitwise AND: &
// Bitwise OR: | ---> Note: Commutative operation (changing the order of the operands does not change the result)
// Bitwise XOR: ^ ---> Note: Commutative operation (changing the order of the operands does not change the result), also 0^x = x and x^x = 0 
// Bitwise complement: ~

// Quick table:
// a | b = a or b
// a !| b = a nor b
// a & b = a and b
// a !& b = a nand b
// a ^ b = a xor b
// a !^ b = a xnor b
// !a = not a